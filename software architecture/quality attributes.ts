/* 
1. Performance

    Response time, throughput, and latency under normal and peak loads.
    Example: API requests must respond in under 300 ms for 95% of users.

2. Scalability

    Ability to handle increased load without performance degradation.
    Example: System should scale horizontally to support 10× user growth.

3. Availability

    The percentage of uptime guaranteed over time.
    Example: 99.9% uptime per month.

4. Reliability

    System’s ability to operate correctly under defined conditions.
    Example: System should recover automatically from transient failures.

5. Security

    Authentication, authorization, encryption, and audit logging.
    Example: All sensitive data must be encrypted at rest and in transit.

6. Maintainability

    Ease of updating, fixing, or improving the system.
    Example: Each service must be deployable independently.

7. Usability

    How intuitive and easy the system is for users.
    Example: New users should be able to complete onboarding within 2 minutes.

8. Observability

    Ability to measure and understand system behavior via logs, metrics, and traces.
    Example: Every request should include a trace ID for correlation.

9. Portability

    Ability to run across different environments (e.g., cloud providers, OS).
    Example: System must run on AWS, Azure, or on-prem without code changes.

10. Compliance

    Meeting regulatory or organizational standards (e.g., GDPR, HIPAA).
    Example: User data must be deleted within 30 days of account deletion.

*/